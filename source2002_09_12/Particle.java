public class Particle {
  private float x;
  private float y;
  private float xv;
  private float yv;
  public float getX() {
    return x;
  }

  public float getXv() {
    return xv;
  }

  public float getY() {
    return y;
  }

  public float getYv() {
    return yv;
  }

  public void setX(float x) {
    this.x = x;
  }

  public void setXv(float xv) {
    this.xv = xv;
  }

  public void setY(float y) {
    this.y = y;
  }

  public void setYv(float yv) {
    this.yv = yv;
  }

}
